}

function syncAddressChoiceForUser(user: AdminOrderFormUserSummary | null) {
  if (!user) {
    state.deliveryAddressChoice = 'none'
    resetDeliveryAddress()
    return
  }

  const existingChoiceBelongsToUser = user.addresses.some(address => address.id === state.deliveryAddressChoice)

  if (state.deliveryAddressChoice === 'new') {
    prefillDeliveryAddressFromUser(user)
    return
  }

  if (existingChoiceBelongsToUser) {
    return
  }

  if (user.addresses.length > 0) {
    state.deliveryAddressChoice = user.addresses.find(address => address.isDefault)?.id ?? user.addresses[0]!.id
    resetDeliveryAddress()
    return
  }

  state.deliveryAddressChoice = 'new'
  prefillDeliveryAddressFromUser(user)
}

