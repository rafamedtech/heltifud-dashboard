function prefillDeliveryAddressFromUser(user: AdminOrderFormUserSummary) {
  state.deliveryAddress.destinatario = state.deliveryAddress.destinatario || formatUserLabel(user)
  state.deliveryAddress.telefono = state.deliveryAddress.telefono || user.telefono || ''
