function cloneEditableSlot(slot?: DetailedMenuSlot | AdminOrderPlanEditorSlotState['slot'] | null) {
  if (!slot) {
    return {
      platilloPrincipal: createEmptyEditableFoodItem(),
      guarnicion1: null,
      guarnicion2: null,
      contenedor: '',
      adicionales: []
    }
  }

  return {
    platilloPrincipal: cloneEditableFoodItem(slot.platilloPrincipal) ?? createEmptyEditableFoodItem(),
    guarnicion1: cloneEditableFoodItem(slot.guarnicion1),
    guarnicion2: cloneEditableFoodItem(slot.guarnicion2),
    contenedor: slot.contenedor ?? '',
    adicionales: slot.adicionales.map(item => cloneEditableFoodItem(item) ?? createEmptyEditableFoodItem())
  }
}

