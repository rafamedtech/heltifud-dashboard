    (maxCount, entry) => Math.max(maxCount, entry.item.slots.length),
    0
  )

  return Array.from({ length: maxSelectionCount }, (_, index) => {
    const entries = editablePlanRows.value
      .map((entry) => {
        const slot = entry.item.slots[index]

        if (!slot) {
          return null
        }

        return {
          row: entry.item,
          rowIndex: entry.index,
          plan: entry.plan,
          slot
        } satisfies UnifiedEditorEntry
      })
      .filter(Boolean)
      .sort((left, right) => {
        const leftEntry = left as UnifiedEditorEntry
        const rightEntry = right as UnifiedEditorEntry
        const byPlanType = PLAN_TYPE_ORDER[leftEntry.plan.tipo] - PLAN_TYPE_ORDER[rightEntry.plan.tipo]

        if (byPlanType !== 0) {
          return byPlanType
        }

        return leftEntry.rowIndex - rightEntry.rowIndex
      }) as UnifiedEditorEntry[]

    const referenceSlot = entries[0]?.slot

    return {
      key: `editor-day-${index + 1}`,
      selectionIndex: index + 1,
      dayOfWeek: referenceSlot?.dayOfWeek ?? 'LUNES',
      menuDayOrder: referenceSlot?.menuDayOrder ?? index + 1,
      cycle: Math.ceil((index + 1) / VISIBLE_DAY_KEYS.length),
      entries
    }
  })
})

