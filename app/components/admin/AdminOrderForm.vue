    dayOfWeek: slot.dayOfWeek,
    menuDayOrder: slot.menuDayOrder,
    slotType: slot.slotType,
    slot: {
      platilloPrincipal: cloneEditableFoodItem(slot.slot.platilloPrincipal),
      guarnicion1: cloneEditableFoodItem(slot.slot.guarnicion1),
      guarnicion2: cloneEditableFoodItem(slot.slot.guarnicion2),
      contenedor: slot.slot.contenedor ?? '',
      adicionales: slot.slot.adicionales.map(item => cloneEditableFoodItem(item))
    }
  })))
