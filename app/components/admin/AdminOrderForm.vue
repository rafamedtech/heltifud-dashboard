function buildDefaultSlotsForPlan(plan: PlanSummary, row: OrderPlanRowState, menu: WeeklyMenuDetail) {
  const requestedDishCount = getRequestedDishCount(row, plan)

  if (requestedDishCount <= 0) {
    return []
  }

  const slotKey = PLAN_TYPE_TO_SLOT_KEY[plan.tipo]
  const slotType = SLOT_KEY_TO_MENU_SLOT_TYPE[slotKey]
  const templates = getVisibleMenuDays(menu).map((day) => {
    const sourceSlot = day[slotKey]

    return {
      sourceWeeklyMenuId: menu.id,
      sourceMenuDayId: day.sourceMenuDayId ?? null,
      sourceDaySlotId: sourceSlot.sourceDaySlotId ?? null,
      dayOfWeek: day.dayOfWeek,
      menuDayOrder: day.menuDayOrder ?? DAY_ORDER_MAP[day.dayOfWeek],
      slotType,
      slot: cloneEditableSlot(sourceSlot)
    }
  })

  if (!templates.length) {
    return []
  }

  return Array.from({ length: requestedDishCount }, (_, index) => {
    const template = templates[index % templates.length]!

    return {
      sourceWeeklyMenuId: template.sourceWeeklyMenuId,
      sourceMenuDayId: template.sourceMenuDayId,
      sourceDaySlotId: template.sourceDaySlotId,
      selectionIndex: index + 1,
      dayOfWeek: template.dayOfWeek,
      menuDayOrder: template.menuDayOrder,
      slotType: template.slotType,
      slot: cloneEditableSlot(template.slot)
    } satisfies AdminOrderPlanEditorSlotState
  })
}

function syncRowEditorFromCurrentMenu(row: OrderPlanRowState) {
  const plan = row.planId ? plansById.value.get(row.planId) ?? null : null

  if (!selectedMenuDetail.value || !plan || !row.quantity || row.quantity <= 0) {
    clearRowSlots(row)
    return
  }

  setRowSlots(row, buildDefaultSlotsForPlan(plan, row, selectedMenuDetail.value))
}

function syncAllPlanEditorsFromCurrentMenu() {
  state.planItems.forEach(row => syncRowEditorFromCurrentMenu(row))
}

async function syncSelectedMenuDetail(menuId: string) {
  if (!menuId) {
    selectedMenuDetail.value = null
    return
  }

  isMenuDetailLoading.value = true

  try {
    selectedMenuDetail.value = await $fetch<WeeklyMenuDetail>(`/api/menu/${menuId}`)
  } catch (error) {
    selectedMenuDetail.value = null

    if (import.meta.client) {
      toast.add({
        title: 'No pudimos cargar el menú seleccionado',
        description: error instanceof Error ? error.message : 'Intenta de nuevo en unos segundos.',
        color: 'error',
        icon: 'i-lucide-circle-alert'
      })
    }
  } finally {
    isMenuDetailLoading.value = false
  }
}

async function applyWeeklyMenuChange(nextMenuId: string) {
  state.weeklyMenuId = nextMenuId
  await syncSelectedMenuDetail(nextMenuId)
  syncAllPlanEditorsFromCurrentMenu()
}

async function handleWeeklyMenuChange(value: string | number | undefined) {
  const rawValue = typeof value === 'string' ? value : ''
  const nextMenuId = rawValue === NO_WEEKLY_MENU_VALUE ? '' : rawValue

  if (nextMenuId === state.weeklyMenuId) {
    return
  }

  if (state.planItems.some(row => rowHasManualSlotEdits(row))) {
    pendingRegeneration.value = {
      kind: 'weekly-menu',
      nextMenuId
    }
    regenerationConfirmOpen.value = true
    return
  }

  await applyWeeklyMenuChange(nextMenuId)
}

function buildPlanRowsFromSelection(nextPlanIds: string[]) {
  const currentRowsByPlanId = new Map(
    state.planItems
      .filter(item => item.planId)
      .map(item => [item.planId, item])
  )

  state.planItems = nextPlanIds.map((planId) => {
    const existing = currentRowsByPlanId.get(planId)

    if (existing) {
      return existing
    }

    const newRow = createEmptyPlanRow()
    newRow.planId = planId
    syncRowEditorFromCurrentMenu(newRow)
    return newRow
  })
}

function handlePlanSelectionChange(value: string[] | undefined) {
  const nextPlanIds = (value ?? []).filter(Boolean)
  const removingEditedPlan = state.planItems.some(item =>
    item.planId
    && !nextPlanIds.includes(item.planId)
    && rowHasManualSlotEdits(item)
  )

  if (removingEditedPlan) {
    pendingRegeneration.value = {
      kind: 'plan-selection',
      nextPlanIds
    }
    regenerationConfirmOpen.value = true
    return
  }

  buildPlanRowsFromSelection(nextPlanIds)
}

function applyQuantityChange(row: OrderPlanRowState, nextQuantity: number | null) {
  row.quantity = nextQuantity
  syncRowEditorFromCurrentMenu(row)
}

function handleQuantityChange(row: OrderPlanRowState, value: number | undefined) {
  const nextQuantity = value === undefined ? null : Number(value)

  if (nextQuantity === row.quantity) {
    return
  }

  if (selectedMenuDetail.value && rowHasManualSlotEdits(row)) {
    pendingRegeneration.value = {
      kind: 'quantity-change',
      rowKey: row.key,
      nextQuantity
    }
    regenerationConfirmOpen.value = true
    return
  }

  applyQuantityChange(row, nextQuantity)
}

async function confirmRegeneration() {
  const action = pendingRegeneration.value

  if (!action) {
    return
  }

  regenerationConfirmOpen.value = false
  pendingRegeneration.value = null

  if (action.kind === 'weekly-menu') {
    await applyWeeklyMenuChange(action.nextMenuId)
    return
  }

  if (action.kind === 'plan-selection') {
    buildPlanRowsFromSelection(action.nextPlanIds)
    return
  }

  const row = state.planItems.find(item => item.key === action.rowKey)

  if (!row) {
    return
  }

  applyQuantityChange(row, action.nextQuantity)
}

function cancelRegeneration() {
  regenerationConfirmOpen.value = false
  pendingRegeneration.value = null
}

function removePlanRow(index: number) {
  const nextPlanIds = state.planItems
    .filter((_, itemIndex) => itemIndex !== index)
    .map(item => item.planId)
    .filter(Boolean)

  handlePlanSelectionChange(nextPlanIds)
}

function getPlanSlotError(rowIndex: number, selectionIndex: number, field: 'platilloPrincipal' | 'contenedor') {
  if (!hasTriedSubmit.value) {
    return undefined
  }

  const row = state.planItems[rowIndex]

  if (!row) {
    return undefined
  }

  const slotIndex = row.slots.findIndex(slot => slot.selectionIndex === selectionIndex)

  if (slotIndex < 0) {
    return undefined
  }

  return slotFieldErrors.value[`${rowIndex}.${slotIndex}.${field}`]
}

async function onSubmit() {
  if (isSubmitting.value || !hasAnyFormData.value) {
    return
  }

  clearFieldErrors()
  hasTriedSubmit.value = true

  if (!validationResult.value.success) {
    validationResult.value.error.issues.forEach((issue) => {
      const name = issue.path.join('.')

      if (!name || fieldErrors[name]) {
        return
      }

      fieldErrors[name] = issue.message
    })

    toast.add({
      title: 'El pedido no se pudo guardar',
      description: 'Revisa los campos obligatorios antes de continuar.',
      color: 'error',
      icon: 'i-lucide-circle-alert'
    })
    return
  }

  isSubmitting.value = true

  try {
    const saved = await saveOrder(orderPayload.value)
    initialSnapshot.value = serializeFormState()
    toast.add({
      title: 'Pedido creado',
      color: 'success',
      icon: 'i-lucide-check-circle'
    })
    emit('saved', saved)
  } catch (error) {
    toast.add({
      title: 'No pudimos guardar el pedido',
      description: error instanceof Error ? error.message : 'Intenta de nuevo en unos segundos.',
      color: 'error',
      icon: 'i-lucide-circle-alert'
    })
  } finally {
    isSubmitting.value = false
  }
}

